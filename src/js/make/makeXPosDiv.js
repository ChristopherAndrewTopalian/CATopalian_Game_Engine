function makeXPosDiv()
{
    let xPosDiv = ce('div');
    xPosDiv.id = 'xPosDiv';
    xPosDiv.textContent = 'x';
    xPosDiv.className = 'textStyle001';
    xPosDiv.title = 'makeXPosDiv()';
    xPosDiv.style.position = 'fixed';
    xPosDiv.style.right = '0px';
    xPosDiv.style.bottom = '375px';
    xPosDiv.style.zIndex = 1000;
    xPosDiv.style.color = 'rgb(255, 150, 150)';
    xPosDiv.style.textShadow = '1px 1px rgb(0, 0, 0)';
    ba(xPosDiv);
}

